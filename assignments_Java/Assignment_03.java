package assignments_Java;

// call multiple  static method

public class Assignment_03 {
	static void method_01()
	{
		System.out.println("Static method 1 is running");
	}
	static void method_02()
	{
		System.out.println("Static method 2 is running");
	}
	
	static void method_03()
	{
		System.out.println("Static method 3 is running");
	}
	
	public static void main(String[] args) 
	{
		method_01();
		method_02();
		method_03();
				
	}

}
